CREATE TABLE TB_USUARIO (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 26) PRIMARY KEY,
    email VARCHAR2(100) NOT NULL UNIQUE,
    senha VARCHAR2(255) NOT NULL,
    role VARCHAR2(20) NOT NULL
);
CREATE TABLE TB_ADMIN (
    usuario_id NUMBER PRIMARY KEY,
    CONSTRAINT fk_admin_usuario FOREIGN KEY (usuario_id) REFERENCES TB_USUARIO(id) ON DELETE CASCADE
);
CREATE TABLE TB_COLABORADOR (
    usuario_id NUMBER PRIMARY KEY,
    nome VARCHAR2(100) NOT NULL,
    cpf VARCHAR2(14) NOT NULL UNIQUE,
    tipo_colaborador VARCHAR2(30) NOT NULL,
    CONSTRAINT fk_colaborador_usuario FOREIGN KEY (usuario_id) REFERENCES TB_USUARIO(id) ON DELETE CASCADE
);
CREATE TABLE TB_PACIENTE (
    usuario_id NUMBER PRIMARY KEY,
    nome VARCHAR2(100) NOT NULL,
    cpf VARCHAR2(14) NOT NULL UNIQUE,
    tipo_sanguineo VARCHAR2(20),
    altura NUMBER(3,2),
    CONSTRAINT fk_paciente_usuario FOREIGN KEY (usuario_id) REFERENCES TB_USUARIO(id) ON DELETE CASCADE
);
CREATE TABLE TB_PACIENTE_ALERGIAS (
    paciente_id NUMBER NOT NULL,
    alergia VARCHAR2(100),
    CONSTRAINT fk_alergias_paciente FOREIGN KEY (paciente_id) REFERENCES TB_PACIENTE(usuario_id) ON DELETE CASCADE
);
CREATE TABLE TB_UNIDADE_SAUDE (
    id NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    nome VARCHAR2(100) NOT NULL,
    endereco VARCHAR2(255) NOT NULL
);
CREATE TABLE TB_SALA (
    id NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    numero VARCHAR2(10) NOT NULL,
    nome VARCHAR2(50),
    disponibilidade VARCHAR2(100),
    unidade_id NUMBER NOT NULL,
    CONSTRAINT fk_sala_unidade_saude FOREIGN KEY (unidade_id) REFERENCES TB_UNIDADE_SAUDE(id) ON DELETE CASCADE
);
CREATE TABLE TB_AGENDAMENTO (
    id NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    paciente_id NUMBER NOT NULL,
    medico_id NUMBER NOT NULL,
    unidade_id NUMBER NOT NULL,
    sala_id NUMBER NOT NULL,
    data_hora_inicio TIMESTAMP NOT NULL,
    data_hora_fim TIMESTAMP NOT NULL,
    tipo VARCHAR2(20) NOT NULL,
    status VARCHAR2(20) DEFAULT 'AGENDADO' NOT NULL,
    especialidade VARCHAR2(100) NOT NULL,

    -- Chaves Estrangeiras (Relacionamentos conforme Requisito)
    CONSTRAINT fk_age_paciente FOREIGN KEY (paciente_id) REFERENCES TB_PACIENTE(usuario_id),
    CONSTRAINT fk_age_medico FOREIGN KEY (medico_id) REFERENCES TB_COLABORADOR(usuario_id),
    CONSTRAINT fk_age_unidade FOREIGN KEY (unidade_id) REFERENCES TB_UNIDADE_SAUDE(id),
    CONSTRAINT fk_age_sala FOREIGN KEY (sala_id) REFERENCES TB_SALA(id) ON DELETE CASCADE
);


COMMIT;